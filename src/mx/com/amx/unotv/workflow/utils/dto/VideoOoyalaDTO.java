package mx.com.amx.unotv.workflow.utils.dto;

public class VideoOoyalaDTO {
	
	private String source;
	private String alternate_text;
	private String duration;
	private String fileSize;
	
	/**
	 * @return the source
	 */
	public String getSource() {
		return source;
	}
	/**
	 * @param source the source to set
	 */
	public void setSource(String source) {
		this.source = source;
	}
	/**
	 * @return the alternate_text
	 */
	public String getAlternate_text() {
		return alternate_text;
	}
	/**
	 * @param alternate_text the alternate_text to set
	 */
	public void setAlternate_text(String alternate_text) {
		this.alternate_text = alternate_text;
	}
	/**
	 * @return the duration
	 */
	public String getDuration() {
		return duration;
	}
	/**
	 * @param duration the duration to set
	 */
	public void setDuration(String duration) {
		this.duration = duration;
	}
	/**
	 * @return the fileSize
	 */
	public String getFileSize() {
		return fileSize;
	}
	/**
	 * @param fileSize the fileSize to set
	 */
	public void setFileSize(String fileSize) {
		this.fileSize = fileSize;
	}
	
	
}
